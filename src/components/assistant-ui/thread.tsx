function getModelIcon(key: string): ReactNode {
  if (key.startsWith('claude')) return <ClaudeIcon className="size-4" />;
  if (key.startsWith('gemini')) return <GeminiIcon className="size-4" />;
  return <Sparkles className="size-4" />;
}

// Map our MODELS to assistant-ui ModelOption format
const MODEL_OPTIONS: ModelOption[] = MODELS.map((m) => ({
  id: m.key,
  name: m.displayName,
  description: m.badge,
  icon: getModelIcon(m.key),
}));

